     public void mostrarRepuestos() {
        List<Repuesto> ListaDeRepuestos = repuestoService.obtenerTodosLosRepuestos();
        DefaultTableModel model = (DefaultTableModel) visorRepuestos.getModel();
        model.setRowCount(0); // Limpiar la tabla antes de agregar nuevos datos

        for (Repuesto re : ListaDeRepuestos) {
            model.addRow(new Object[]{re.getIdRepuesto(), re.getDescripcion(), re.getStock(), re.getPrecio()});
        }
    }
     
    
    
    

